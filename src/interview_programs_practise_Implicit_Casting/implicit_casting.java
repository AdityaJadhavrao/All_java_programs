package interview_programs_practise_Implicit_Casting;

public class implicit_casting {
	
	public static void main(String args[])
	{
		Integer a = 1234;
		double b= a;
		
		System.out.println(b);
		
		char c = 'a';
		int d = c;
		System.out.println(d);
		
		float f = 24.24f;
		double i = f;
		System.out.println(i);
	}

}
